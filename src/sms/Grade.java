     private void loadStudents() {
        studentCombo.removeAllItems();
        try (Connection c = Dbconnection.getConnection();
             ResultSet rs = c.createStatement().executeQuery("SELECT id, name FROM students")) {
            while (rs.next()) {
                studentCombo.addItem(rs.getInt("id") + " - " + rs.getString("name"));
            }
        } catch (Exception e) { e.printStackTrace(); }
    }

    // ---------------- Load Courses ----------------
    private void loadCourses() {
        courseCombo.removeAllItems();
        try (Connection c = Dbconnection.getConnection();
             ResultSet rs = c.createStatement().executeQuery("SELECT id, course_name FROM courses")) {
            while (rs.next()) {
                courseCombo.addItem(rs.getInt("id") + " - " + rs.getString("course_name"));
            }
        } catch (Exception e) { e.printStackTrace(); }
    }
    private void loadAllGrades() {
    model.setRowCount(0);
    String sql = "SELECT g.id, g.student_id, g.course_id, g.grade FROM grades g";

    try (Connection c = Dbconnection.getConnection();
         PreparedStatement ps = c.prepareStatement(sql);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getInt("student_id"),
                rs.getInt("course_id"),
                rs.getString("grade")
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}
 void displayReport(int studentId) {
        String sql = "SELECT s.name, c.course_name, g.grade " +
                     "FROM grades g " +
                     "JOIN students s ON g.student_id = s.id " +
                     "JOIN courses c ON g.course_id = c.id " +
                     "WHERE s.id=?";

        try (Connection c = Dbconnection.getConnection();
             PreparedStatement ps = c.prepareStatement(sql)) {
            ps.setInt(1, studentId);
            ResultSet rs = ps.executeQuery();

            String studentName = "";
            StringBuilder report = new StringBuilder();

            while (rs.next()) {
                if (studentName.isEmpty()) {
                    studentName = rs.getString("name");
                    report.append("STUDENT REPORT\n\n");
                    report.append("Name: ").append(studentName).append("\n\n");
                    report.append(String.format("%-30s %-10s\n", "Course", "Grade"));
                    report.append("-------------------------------------\n");
                }
                report.append(String.format("%-30s %-10s\n", rs.getString("course_name"), rs.getString("grade")));
            }

            JTextArea area = new JTextArea(report.toString());
            area.setEditable(false);
            area.setFont(new Font("Monospaced", Font.PLAIN, 12));
            JOptionPane.showMessageDialog(this, new JScrollPane(area), "Report", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            e.printStackTrace();
        }
 }
