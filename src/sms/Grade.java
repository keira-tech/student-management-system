    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int row = table1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a student first");
            return;
        }

        int studentId = (int) model.getValueAt(row, 1);

        String sql = "SELECT s.name, c.course_name, g.grade " +
                     "FROM grades g " +
                     "JOIN students s ON g.student_id = s.id " +
                     "JOIN courses c ON g.course_id = c.id " +
                     "WHERE s.id=?";

        try (Connection c = Dbconnection.getConnection();
             PreparedStatement ps = c.prepareStatement(sql)) {

            ps.setInt(1, studentId);
            ResultSet rs = ps.executeQuery();

            if (!rs.isBeforeFirst()) { // no rows
                JOptionPane.showMessageDialog(this, "No data to export");
                return;
            }

            rs.next(); // move to first row to get student name
            String studentName = rs.getString("name");

            JFileChooser chooser = new JFileChooser();
            chooser.setSelectedFile(new java.io.File(studentName + "_Report.csv"));
            int choice = chooser.showSaveDialog(this);
            if (choice != JFileChooser.APPROVE_OPTION) return;

            try (PrintWriter pw = new PrintWriter(new FileWriter(chooser.getSelectedFile()))) {
                pw.println("Student Name," + studentName);
                pw.println("Course,Grade");
                pw.println(rs.getString("course_name") + "," + rs.getString("grade"));

                while (rs.next()) {
                    pw.println(rs.getString("course_name") + "," + rs.getString("grade"));
                }
            }

            JOptionPane.showMessageDialog(this, "Report exported successfully!");

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        if (studentCombo.getSelectedItem() == null || courseCombo.getSelectedItem() == null || txtGrade.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fill all fields");
            return;
        }

        int studentId = Integer.parseInt(studentCombo.getSelectedItem().toString().split(" - ")[0]);
        int courseId = Integer.parseInt(courseCombo.getSelectedItem().toString().split(" - ")[0]);
     String grade = txtGrade.getText().trim().toUpperCase();

    // Validate grade
    if (!grade.matches("[A-D][+-]?|F")) {
        JOptionPane.showMessageDialog(this, "Invalid grade! Allowed: A, A+, A-, B, B+, B-, C, C+, C-, D, D+, D-, F");
        txtGrade.requestFocus();
        return;
    }


        try (Connection c = Dbconnection.getConnection();
             PreparedStatement ps = c.prepareStatement("INSERT INTO grades(student_id, course_id, grade) VALUES(?,?,?)")) {

            ps.setInt(1, studentId);
            ps.setInt(2, courseId);
            ps.setString(3, grade);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Grade added!");
            // refresh table
            loadAllGrades();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

