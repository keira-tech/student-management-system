    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int studentIndex = CbStudents.getSelectedIndex();
        if (studentIndex == -1) {
            JOptionPane.showMessageDialog(this, "Select a student!");
            return;
        }
        int studentId = studentIds.get(studentIndex);

        int[] selectedIndices = listCourses.getSelectedIndices();
        if (selectedIndices.length == 0) {
            JOptionPane.showMessageDialog(this, "Select at least one course!");
            return;
        }

        try (Connection c = Dbconnection.getConnection()) {
            String sql = "INSERT INTO student_courses(student_id, course_id) VALUES (?, ?)";
            PreparedStatement ps = c.prepareStatement(sql);

           for (int idx : selectedIndices) {
    int courseId = courseIds.get(idx);

    // 🔍 Check if already assigned
    PreparedStatement check = c.prepareStatement(
        "SELECT * FROM student_courses WHERE student_id=? AND course_id=?"
    );
    check.setInt(1, studentId);
    check.setInt(2, courseId);
    ResultSet rs = check.executeQuery();

    if (rs.next()) {
        continue; // skip duplicate
    }

    ps.setInt(1, studentId);
    ps.setInt(2, courseId);
    ps.addBatch();
}
            ps.executeBatch();
            JOptionPane.showMessageDialog(this, "Courses assigned successfully!");
            loadAssignments();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error assigning courses: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select an assignment to remove!");
            return;
        }

        String studentName = model.getValueAt(row, 0).toString();
        String courseName = model.getValueAt(row, 1).toString();

        String sql = "DELETE sc FROM student_courses sc " +
                     "JOIN students s ON sc.student_id = s.id " +
                     "JOIN courses c ON sc.course_id = c.id " +
                     "WHERE s.name=? AND c.course_name=?";
        try (Connection c = Dbconnection.getConnection();
             PreparedStatement ps = c.prepareStatement(sql)) {

            ps.setString(1, studentName);
            ps.setString(2, courseName);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Assignment removed successfully!");
            loadAssignments();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
         int row = table.getSelectedRow();

    selectedStudentName = model.getValueAt(row, 0).toString();

    // Set student in combo
    CbStudents.setSelectedItem(selectedStudentName);

    // Clear previous selection
    listCourses.clearSelection();

    try (Connection c = Dbconnection.getConnection()) {

        PreparedStatement ps = c.prepareStatement(
            "SELECT c.course_name FROM student_courses sc " +
            "JOIN students s ON sc.student_id = s.id " +
            "JOIN courses c ON sc.course_id = c.id " +
            "WHERE s.name=?"
        );

        ps.setString(1, selectedStudentName);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            String courseName = rs.getString("course_name");

            // match with JList
            for (int i = 0; i < listCourses.getModel().getSize(); i++) {
                if (listCourses.getModel().getElementAt(i).equals(courseName)) {
                    listCourses.addSelectionInterval(i, i);
                }
            }
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_tableMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          if (selectedStudentName == null) {
        JOptionPane.showMessageDialog(this, "Select a record first!");
        return;
    }

    int[] selectedIndices = listCourses.getSelectedIndices();
    if (selectedIndices.length == 0) {
        JOptionPane.showMessageDialog(this, "Select at least one course!");
        return;
    }

    try (Connection c = Dbconnection.getConnection()) {

        // ❌ Delete old assignments
        PreparedStatement delete = c.prepareStatement(
            "DELETE sc FROM student_courses sc " +
            "JOIN students s ON sc.student_id = s.id " +
            "WHERE s.name=?"
        );
        delete.setString(1, selectedStudentName);
        delete.executeUpdate();

        // ✅ Insert new assignments
        PreparedStatement insert = c.prepareStatement(
            "INSERT INTO student_courses(student_id, course_id) " +
            "SELECT s.id, c.id FROM students s, courses c " +
            "WHERE s.name=? AND c.course_name=?"
        );

        for (int idx : selectedIndices) {
            String courseName = listCourses.getModel().getElementAt(idx);

            insert.setString(1, selectedStudentName);
            insert.setString(2, courseName);
            insert.addBatch();
        }

        insert.executeBatch();

        JOptionPane.showMessageDialog(this, "Updated successfully!");
        loadAssignments();

        // reset
        selectedStudentName = null;
        listCourses.clearSelection();

    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

