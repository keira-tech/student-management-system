      private void loadStudents() {
        studentIds.clear();
        CbStudents.removeAllItems();
        try (Connection c = Dbconnection.getConnection();
             Statement st = c.createStatement();
             ResultSet rs = st.executeQuery("SELECT id, name FROM students")) {

            while (rs.next()) {
                studentIds.add(rs.getInt("id"));
                    CbStudents.addItem(rs.getString("name"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // Load courses into JList
    private void loadCourses() {
        courseIds.clear();
        DefaultListModel<String> modelCourses = new DefaultListModel<>();
        try (Connection c = Dbconnection.getConnection();
             Statement st = c.createStatement();
             ResultSet rs = st.executeQuery("SELECT id, course_name FROM courses")) {

            while (rs.next()) {
                courseIds.add(rs.getInt("id"));
                modelCourses.addElement(rs.getString("course_name"));
            }
            listCourses.setModel(modelCourses);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // Load current assignments into table
    private void loadAssignments() {
        model.setRowCount(0);
        String sql = "SELECT s.name AS student, c.course_name AS course " +
                     "FROM student_courses sc " +
                     "JOIN students s ON sc.student_id = s.id " +
                     "JOIN courses c ON sc.course_id = c.id";
        try (Connection c = Dbconnection.getConnection();
             Statement st = c.createStatement();
             ResultSet rs = st.executeQuery(sql)) {

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("student"), rs.getString("course")});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

